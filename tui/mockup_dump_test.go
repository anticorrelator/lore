package main

// Mockup-dump harness: composes fixture-populated root models at fixed
// dimensions and writes their View() frames to .ans files, so layout
// candidates can be rendered and compared (via freeze) without driving a
// live terminal. Fixture data only — nothing here reads a live store.
//
// Run:
//
//	LORE_MOCKUP_DUMP=1 go test -run TestMockupDump -v
//
// Without LORE_MOCKUP_DUMP=1 every dump test skips, so the harness is inert
// during normal `go test`. Output lands in $LORE_MOCKUP_DIR when set,
// otherwise in the tui-design-exploration-pass work item's captures/
// directory under ~/.lore.
//
// Adding variants — the extension contract:
//
// Each new group of variants lives in its OWN standalone test file (e.g.
// mockup_axis_a_test.go) in this package. A variant is a test function (or
// subtest) that builds a fixture model, renders a frame, and hands it to
// dumpMockup:
//
//	func TestMockupDumpAxisA(t *testing.T) {
//		dumpMockup(t, "cand-worklist-flat", func(t *testing.T) string {
//			m := newMockupModel(t, stateWork, config.LayoutTopBottom,
//				mockupWidth, mockupHeight, myFixtureItems(), nil)
//			return m.viewContent()
//		})
//	}
//
// Do not edit this file to register a variant — there is no registry. Test
// names are the selection mechanism: `go test -run 'TestMockupDumpAxisA'`
// renders one file's variants, `-run 'TestMockupDump'` renders everything.
// dumpMockup provides the skip gate, the output directory, and a
// determinism check (each frame is rendered twice and must be
// byte-identical) — new fixtures that render nondeterministically fail
// instead of producing unstable captures.
//
// Fixture rows flow through the real list/collection render paths; never
// pre-style fixture strings — plain-text truncation before styling is the
// engine's job.

