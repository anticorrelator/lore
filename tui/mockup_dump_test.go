// mockupWidth/mockupHeight match the tmux capture standard (170×46) so
// headless dumps stay pixel-comparable with live captures.
const (
	mockupWidth  = 170
	mockupHeight = 46
)

// requireMockupDump skips the test unless the dump harness is explicitly
// enabled, mirroring the visual_dump_test.go probe precedent.
func requireMockupDump(t *testing.T) {
	t.Helper()
	if os.Getenv("LORE_MOCKUP_DUMP") != "1" {
		t.Skip("mockup dump disabled — set LORE_MOCKUP_DUMP=1 to write .ans captures")
	}
}

// mockupOutDir resolves the capture output directory: $LORE_MOCKUP_DIR when
// set, otherwise the design-exploration work item's captures/ directory.
// The directory is created if missing.
func mockupOutDir(t *testing.T) string {
	t.Helper()
	dir := os.Getenv("LORE_MOCKUP_DIR")
	if dir == "" {
		home, err := os.UserHomeDir()
		if err != nil {
			t.Fatalf("resolving home dir for default capture path: %v", err)
		}
		dir = filepath.Join(home, ".lore", "repos", "github.com", "anticorrelator", "lore",
			"_work", "tui-design-exploration-pass", "captures")
	}
	if err := os.MkdirAll(dir, 0o755); err != nil {
		t.Fatalf("creating capture dir %s: %v", dir, err)
	}
	return dir
}

// dumpMockup is the single entry point for every variant: it applies the
// skip gate, renders the frame twice to prove the fixture is deterministic,
// and writes <name>.ans to the output directory.
func dumpMockup(t *testing.T, name string, render func(t *testing.T) string) {
	t.Helper()
	requireMockupDump(t)

	first := render(t)
	second := render(t)
	if first != second {
		t.Fatalf("mockup %q is nondeterministic: two renders differ (len %d vs %d)",
			name, len(first), len(second))
	}
	if first == "" {
		t.Fatalf("mockup %q rendered an empty frame", name)
	}

	frame := first
	if frame[len(frame)-1] != '\n' {
		frame += "\n"
	}
	path := filepath.Join(mockupOutDir(t), name+".ans")
	if err := os.WriteFile(path, []byte(frame), 0o644); err != nil {
		t.Fatalf("writing %s: %v", path, err)
	}
	t.Logf("wrote %s (%d bytes)", path, len(frame))
}

// newMockupModel builds a root model populated with fixture lists and sized
// via a real WindowSizeMsg, so every sub-model receives the same dimension
// fan-out as production. Returned commands are discarded — no fixture path
// may depend on executing a Cmd (that is where disk and subprocess I/O live).
func newMockupModel(t *testing.T, state appState, layout config.LayoutMode, width, height int, workItems []work.WorkItem, fuItems []followup.FollowUpItem) model {
	t.Helper()
	m := minimalModel(state, workItems, fuItems)
	m.layoutMode = layout
	m, _ = updateModel(t, m, tea.WindowSizeMsg{Width: width, Height: height})
	return m
}

// withWorkDetail points the detail pane at a fixture detail document,
// mirroring loadDetail's size-then-load sequence without its disk-read Cmd.
func withWorkDetail(t *testing.T, m model, detail *work.WorkItemDetail) model {
	t.Helper()
	m.detail = work.NewDetailModel("", detail.Slug)
	m.detail, _ = m.detail.Update(tea.WindowSizeMsg{Width: m.rightPanelWidth(), Height: m.detailPanelHeight()})
	m.detail, _ = m.detail.Update(work.DetailLoadedMsg{Slug: detail.Slug, Detail: detail})
	m.list.SetCursorBySlug(detail.Slug)
	return m
}

// withSettlementStatus injects a fixture settlement status. Call after
// newMockupModel so the panel is already sized.
func withSettlementStatus(m model, st settlement.Status) model {
	m.settlement = m.settlement.ReplaceStatus(st)
	return m
}

// withPRStatuses marks the PR loader as completed with fixture statuses so
// PR badges render their real states instead of the "..." loading badge.
func withPRStatuses(m model, statuses map[string]gh.PRStatus) model {
	m.list, _ = m.list.Update(work.PRStatusMsg{Statuses: statuses})
	return m
}

// mockupTimeAgo returns an RFC3339 timestamp the given duration before the
// current hour boundary. Truncating to the hour keeps raw timestamps (shown
// verbatim in detail metadata) stable across runs within the same hour, and
// relative labels stay stable indefinitely as long as offsets are day-scale
// with a half-day margin (e.g. 36h → always "1d ago"): the truncation adds
// up to an hour of extra age, so hour- or minute-scale offsets would render
// different labels depending on when the dump runs — don't use them.
func mockupTimeAgo(ago time.Duration) string {
	return time.Now().UTC().Truncate(time.Hour).Add(-ago).Format(time.RFC3339)
}

